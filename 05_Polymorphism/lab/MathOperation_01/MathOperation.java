package MathOperation_01;

public class MathOperation {

    public static int add(int first, int second) {
        return first + second;
    }

    public static int add(int first, int second, int tirth){
        return first + second + tirth;
    }

    public static int add(int first, int second, int tirth, int fourth){
        return first + second + tirth + fourth;
    }
}
